class Mision:
	def init(self, nombre="", elevacion=0, velocidad=0, modo_vuelo="", modo_adq="", usuarios_id = 0, id_mision = -1):
		self.nombre = nombre
		self.elevacion = elevacion
		self.velocidad = velocidad
		self.modo_vuelo= modo_vuelo
		self.modo_adq = modo_adq
		self.usuarios_id = usuarios_id
		self.id = id_mision